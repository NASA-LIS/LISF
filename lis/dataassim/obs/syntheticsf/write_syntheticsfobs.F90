! !ROUTINE: write_syntheticsfobs
! \label{write_syntheticsfobs}
! 
! !REVISION HISTORY: 
! 25Jan2008: Sujay Kumar; Initial Specification
! 
! !INTERFACE: 
subroutine write_syntheticsfobs(n, OBS_State)
! !USES: 
  use ESMF
  use LIS_coreMod,    only : LIS_rc, LIS_masterproc
  use LIS_logMod,     only : LIS_verify, LIS_getNextUnitNumber, &
       LIS_releaseUnitNumber
  use LIS_fileIOMod,  only : LIS_create_output_directory
  use LIS_historyMod, only : LIS_writevar_gridded
  use syntheticsfobs_module
  
  implicit none

! !ARGUMENTS: 

  integer,     intent(in)  :: n 
  type(ESMF_State)         :: OBS_State
!
! !DESCRIPTION: 
! 
! writes the transformed (interpolated/upscaled/reprojected)  
! synthetic observations to a file
! 
!EOP
  type(ESMF_Field)         :: smField
  logical                  :: data_update
  real, pointer            :: sfobs(:)
  real                     :: sfobs_unsc(LIS_rc%ngrid(n))
  character*100            :: obsname
  integer                  :: ftn
  integer                  :: status

  call ESMF_AttributeGet(OBS_State, "Data Update Status", & 
       data_update, rc=status)
  call LIS_verify(status,&
       "ESMF_AttributeGet: Data Update Status failed in write_syntheticsfobs")

  if(data_update) then 
     
     call ESMF_StateGet(OBS_State, "Observation01",smField, &
          rc=status)
     call LIS_verify(status, &
          "ESMF_StateGet failed in write_syntheticsfobs")
     
     call ESMF_FieldGet(smField, localDE=0, farrayPtr=sfobs, rc=status)
     call LIS_verify(status,&
          "ESMF_FieldGet failed in write_syntheticsfobs")

     if(synthetic_sm_struc(n)%scal.eq.1) then 
        if(LIS_rc%ngrid(n).gt.0) then 
           call ESMF_AttributeGet(smfield,"Unscaled Obs",&
                sfobs_unsc,itemCount=LIS_rc%ngrid(n),rc=status)
           call LIS_verify(status, 'Error in AttributeGet-Unscaled Obs')
        endif
     endif

     if(LIS_masterproc) then 
        ftn = LIS_getNextUnitNumber()
        call synthetic_sfobsname(obsname)        

        call LIS_create_output_directory('DAOBS')
        open(ftn,file=trim(obsname), form='unformatted')
     endif

     if(synthetic_sm_struc(n)%scal.eq.1) then 
        call LIS_writevar_gridded(ftn,n,sfobs_unsc,wopt="2d gridspace")     
     endif

     call LIS_writevar_gridded(ftn,n,sfobs,wopt="2d gridspace")
     
     if(LIS_masterproc) then 
        call LIS_releaseUnitNumber(ftn)
     endif

  endif  

end subroutine write_syntheticsfobs

!BOP
! !ROUTINE: synthetic_sfobsname
! \label{synthetic_sfobsname}
! 
! !INTERFACE: 
subroutine synthetic_sfobsname(obsname)
! !USES: 
  use LIS_coreMod, only : LIS_rc

! !ARGUMENTS: 
  character(len=*)      :: obsname
! 
! !DESCRIPTION: 
! 
!EOP

  character(len=12) :: cdate1

  write(unit=cdate1, fmt='(i4.4, i2.2, i2.2, i2.2, i2.2)') &
       LIS_rc%yr, LIS_rc%mo, &
       LIS_rc%da, LIS_rc%hr,LIS_rc%mn

  obsname = trim(LIS_rc%odir)//'/DAOBS/'//cdate1(1:6)//'/'//cdate1//   &
            '.1gs4r'
  
end subroutine synthetic_sfobsname
