! !ROUTINE: LIS_rescale_with_linear_scaling
! \label{LIS_rescale_with_linear_scaling}
!
! !INTERFACE:
  subroutine LIS_rescale_with_linear_scaling(&
       n,             & 
       k,             & 
       nbins,         & 
       ntimes,        & 
       obs_xrange,    &       
       obs_cdf,       &
       obs_value) 

       
    implicit none
! 
