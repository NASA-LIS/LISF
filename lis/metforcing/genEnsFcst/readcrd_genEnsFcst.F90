! !ROUTINE: readcrd_genEnsFcst
! \label{readcrd_genEnsFcst}
!
! !REVISION HISTORY:
! 26 Sept 2016; KR Arsenault, Initial Code developed
!
! !INTERFACE:    
subroutine readcrd_genEnsFcst()
! !USES:
  use ESMF
  use LIS_coreMod, only : LIS_rc, LIS_config
  use LIS_logMod,  only : LIS_logunit, LIS_verify, LIS_endrun
  use genEnsFcst_forcingMod, only : genensfcst_struc
!
! !DESCRIPTION:
!
!  This routine reads the options specific to the 
!  meteorological forcing data generated by LDT or 
!  by outside routines/scripts and written
!  in Netcdf format.
!  
!EOP
  implicit none

  integer :: n, rc

  call ESMF_ConfigFindLabel(LIS_config,"Generic ensemble forecast directory:",rc=rc)
  call ESMF_ConfigGetAttribute(LIS_config,genensfcst_struc%directory,rc=rc)

  call ESMF_ConfigFindLabel(LIS_config,"Generic ensemble forecast number of ensemble members:",rc=rc)
  call LIS_verify(rc, 'Generic ensemble forecast number of ensemble members: not defined')
  call ESMF_ConfigGetAttribute(LIS_config,genensfcst_struc%max_ens_members,rc=rc)
!  do n=1,LIS_rc%nnest
!     call ESMF_ConfigGetAttribute(LIS_config,genensfcst_struc(n)%max_ens_members,rc=rc)
!  enddo

  write(LIS_logunit,*) "[INFO] "
  write(LIS_logunit,*) "[INFO] Using the GenEnsFcst forcing dataset"
  write(LIS_logunit,*) "[INFO] Generic ensemble forecast directory: ",&
                       trim(genensfcst_struc%directory)
  write(LIS_logunit,*) "[INFO] Generic ensemble forecast number of members: ",&
                       genensfcst_struc%max_ens_members

end subroutine readcrd_genEnsFcst

